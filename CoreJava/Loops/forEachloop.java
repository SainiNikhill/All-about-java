package Loops;

public class forEachloop {
    // used when you want to loop over arrays or collections without using an index 
    public static void main(String[] args) {
        int [] numbers  = {10,20,30,40};
    for ( int num : numbers ){
        System.out.println("Number:" + num);

    }
    //  looping though arrays or ArrayList
    }
}
