package Operators;

///  assign values to variables in java ( = , += , -= , *=)


public class Assignment {
    public static void main(String[] args) {

        int a = 10 ;
        System.out.println(a);
        a += 10; /// this means a = a+ 10 ;
        System.out.println(a);
        a -= 10;
        System.out.println(a);
        a *= 2 ;
        System.out.println(a);
        a %= 2;
        System.out.println(a);

    }
}
