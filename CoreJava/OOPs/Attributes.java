package OOPs;

public class Attributes {
    int x = 10 ; 


    public static void main(String[] args) {
        Attributes at = new Attributes();
        Attributes at2 = new Attributes();
        at.x = 40;
       
        System.out.println(at.x);
        System.out.println(at2.x);
    }
    
}
